package point;

public class PointVO {

	private int memid;
	private int point1;
	
	public PointVO(int memid,int point1) {
		this.memid = memid;
		this.point1 = point1;
	}
	
	public int getMemid() {
		return memid;
	}
	public void setMemid(int memid) {
		this.memid = memid;
	}
	public int getPoint1() {
		return point1;
	}
	public void setPoint1(int point1) {
		this.point1 = point1;
	}
	
	
	
	
}
